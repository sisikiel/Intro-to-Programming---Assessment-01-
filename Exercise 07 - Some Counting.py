#NUMBER 1 - Count up from 0 to 50 in increments of 1
print("Counting from 0 to 50")
for a in range (0, 51, 1):
    print(a)

#NUMBER 2 - Count down from 50 to 0 in decrements of 1.
print("\nCounting down from 50 to 0") #\n is used to give space in between the codes while running the program.
for b in range (50, -1, -1):
    print(b)

#NUMBER 3 - Count up from 30 to 50 in increments of 1.
print("\nCounting up from 30 to 50")
for c in range (30, 51, 1):
    print(c)

#NUMBER 4 - Count down from 50 to 10 in decrements of 2.
print("\nCount down from 50 to 10 in decrements of 2")
for d in range (50, 8, -2):
    print(d)

#NUMBER 5 - Count up from 100 to 200 in increments of 5.
print("\nCount up from 100 to 200 in increments of 5")
for e in range (100, 205, 5):
    print(e)